import java.util.LinkedList;
import java.util.List;

/**
 * Definition for a binary tree node.
 * public class TreeNode {
 *     int val;
 *     TreeNode left;
 *     TreeNode right;
 *     TreeNode() {}
 *     TreeNode(int val) { this.val = val; }
 *     TreeNode(int val, TreeNode left, TreeNode right) {
 *         this.val = val;
 *         this.left = left;
 *         this.right = right;
 *     }
 * }
 */
//2021/08/31
class Solution {
    // æ–¹å¼ä¸€ï¼šå›æº¯æ³•ï¼Œå…ˆåºéå†+è·¯å¾„è®°å½•ï¼ˆæ·±åº¦ä¼˜å…ˆï¼‰
    LinkedList<List<Integer>> res = new LinkedList<>();
    LinkedList<Integer> path = new LinkedList<>();

    public List<List<Integer>> pathSum(TreeNode root, int sum) {
        recur(root, sum);
        return res;
    }

    void recur(TreeNode root, int tar) {
        if (root == null)
            return;
        path.add(root.val);
        tar -= root.val;
        if (tar == 0 && root.left == null && root.right == null)
            res.add(new LinkedList(path)); // è‹¥ç›´æ¥add pathï¼Œæ·»åŠ ä¸ºå¼•ç”¨å˜é‡ï¼Œä¼šéšpathçš„æ”¹å˜è€Œæ”¹å˜
        recur(root.left, tar); // å·¦å³é€’å½’ï¼Œé¿å…äº†my solutionä¸­å€¼ä¸åŒ¹é…çš„é—®é¢˜
        recur(root.right, tar);
        path.removeLast(); // æ­¤å¤„ä¿è¯äº†pathçš„æ­£ç¡®æ€§ï¼Œæœ‰è¿›æœ‰å‡º
    }
}
